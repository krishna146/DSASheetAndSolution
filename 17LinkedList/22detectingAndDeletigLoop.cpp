// Function to remove a loop in the linked list.
void removeLoop(Node *head)
{
    // code here
    // just remove the loop without losing any nodes
    // detecting loop
    Node *slow = head;
    Node *fast = head;
    while (fast != NULL && fast->next != NULL)
    {
        slow = slow->next;
        fast = fast->next->next;
        if (slow == fast)
        {
            break;
        }
    }
    // loop found
    if (slow == fast)
    {
        // finding beginning Node
        slow = head;
        while (slow != fast)
        {
            slow = slow->next;
            fast = fast->next;
        }
        // deleting the loop
        Node *beginningNode = slow;
        while (slow->next != beginningNode)
        {
            slow = slow->next;
        }
        slow->next = NULL;
    }
}
